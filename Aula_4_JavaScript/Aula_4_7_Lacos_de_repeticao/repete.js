
// Criamos uma variável "repete", que recebe o valor de 1
var repete = 1 

// Enquanto (repete for menor ou igual a 3), acontecerá:
while( repete <= 3 ){

    // Exibirá uma caixa de alerta, com essa mensagem e o valor da variável repete
    alert("Mensagem: "+repete);

    // Soma ao valor da variável "repete" o valor de +1, para chegar a 4 e parar o laço de repetição
    repete = repete + 1;
}